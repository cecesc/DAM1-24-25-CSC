package ud2.ejerciciosbucles;

public class E0307 {
    public static void main(String[] args) {
        System.out.println("Múltiplos de 7 menores que 100:");
        for (int i = 7; i < 100; i += 7) {
            System.out.println(i);
        }
    }
}