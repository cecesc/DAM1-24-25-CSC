package ud2.ejemplos;

public class EjemploFor2 {
    public static void main(String[] args) {
        for (int i = 10; i > 0; i--) {    //inicio del for                                                  
            System.out.print(i + " ");
        }  //fin del for
        System.out.println("\nFin programa");                                                               
    }    
}
